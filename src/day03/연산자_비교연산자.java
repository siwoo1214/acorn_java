package day03;

public class 연산자_비교연산자 {
	public static void main(String[] args) {
		
		int su1=10, su2=5;
		boolean result;
		
		System.out.println(su1>su2);
		System.out.println(su1<su2);
		System.out.println(su1==su2);
		System.out.println(su1<=su2);
		System.out.println(su1>=su2);
		System.out.println(su1!=su2);
		
		result=su1>su2;
		System.out.println(result);
		
	}
}
